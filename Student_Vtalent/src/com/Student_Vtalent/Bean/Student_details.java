package com.Student_Vtalent.Bean;

public class Student_details {
	private String std_id;
	private String std_name;
	private String std_branch;
	private String std_gender;
	private String std_number;

	public String getStd_id() {
		return std_id;
	}

	public void setStd_id(String std_id) {
		this.std_id = std_id;
	}

	public String getStd_name() {
		return std_name;
	}

	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}

	public String getStd_branch() {
		return std_branch;
	}

	public void setStd_branch(String std_branch) {
		this.std_branch = std_branch;
	}

	public String getStd_gender() {
		return std_gender;
	}

	public void setStd_gender(String std_gender) {
		this.std_gender = std_gender;
	}

	public String getStd_number() {
		return std_number;
	}

	public void setStd_number(String std_number) {
		this.std_number = std_number;
	}

}